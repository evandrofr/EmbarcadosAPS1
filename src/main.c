void tone(int freq, int dur){
	int T = 1000000/(2*freq); 
	int idx = (dur * freq)/1000;
	for(int i = 0; i <= idx; i++){
		pio_set(PIOB, BUZ_PIO_IDX_MASK);      // Coloca 1 no pino LED
		delay_us(T);                        // Delay por software de 200 ms
		pio_clear(PIOB, BUZ_PIO_IDX_MASK);    // Coloca 0 no pino do LED
		delay_us(T);
	}
}


// Função de inicialização do uC
void init(void){
	// Initialize the board clock
	sysclk_init();

	// Desativa WatchDog Timer
	WDT->WDT_MR = WDT_MR_WDDIS;
	
	// Ativa o PIO na qual o LED foi conectado
	// para que possamos controlar o LED.
	pmc_enable_periph_clk(BUZ_PIO_ID);
	pio_set_output(BUZ_PIO, BUZ_PIO_IDX_MASK, 0, 0, 0);
	
	// Inicializa PIO do botao
	pmc_enable_periph_clk(BUT_PIO_ID);
	
	
	
	// configura pino ligado ao botão como entrada com um pull-up.
	pio_set_input(BUT_PIO, BUT_PIO_IDX_MASK, PIO_DEFAULT);
	pio_pull_up(BUT_PIO,BUT_PIO_IDX_MASK,1);

}


int main(void) {
	init();
	int go;
	// super loop
	// aplicacoes embarcadas não devem sair do while(1).
	while (1) {
		
		go = pio_get(BUT_PIO, PIO_INPUT, BUT_PIO_IDX_MASK);
		if(!go){
			for (int i=0;i < sizeof(notes);i++){
				int adj = 2;
				int wait = duration[i] * songspeed/adj;
			
				
				tone(notes[i],duration[i]/adj);      // Coloca 1 no pino
				delay_ms(wait);                        // Delay
				pio_clear(PIOB, BUZ_PIO_IDX_MASK);    // Coloca 0 no pino
				delay_ms(wait);                        // Delay
				
			}
			 //delay is used so it doesn't go to the next loop before tone is finished playing
			//You can click reset on Arduino to replay the song

			} else {
			pio_set(PIOB, BUZ_PIO_IDX_MASK);
		}
		
	}
	return 0;
}